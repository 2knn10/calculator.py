operator =  input("Please Enter The operator : ")
num1 = int(input("Enter Number : " ))
num2 = int(input("Enter Two Number : " ))
    
if operator == "+":
        print(num1 + num2 )
elif operator == "-":
        print(num1 - num2 )
elif operator == "/":
        print(num1 / num2 )
elif operator == "*":
        print(num1 * num2 )
else:
    print("Error")
    
